    private void getNewData() {

        try {
            String str;
            // populate deptno field
            rs = dbCon.executeStatement("SELECT customerID, cfname, clname, address, sex, email, phoneNo FROM proj_customers ORDER BY customerID ASC"); 
            rsCustno = dbCon.executeStatement("SELECT unique sex FROM proj_customers ORDER BY sex ASC"); // TODO: CONFIRM w/ DR. WISSAM
//            rsdeptno = statement.executeQuery("SELECT deptno, dname FROM dept ORDER BY deptno ASC ");
            cmbSex.removeAllItems();
            while (rsCustno.next()) {
                cmbSex.addItem(rsCustno.getString("sex"));
            }

            // populate rest of fields
            rs.beforeFirst();
            rs.first();
            populateFields();
        } catch (SQLException e) {
            javax.swing.JLabel label = new javax.swing.JLabel("SQL Error - Display selected customerID.");
            label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
            JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }
    
