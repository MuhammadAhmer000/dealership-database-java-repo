     private boolean isValidEmail(String email) throws SQLException {
        boolean isValidEmail = false;
        // write code here
        Pattern pattern = Pattern.compile(".+@.+\\..+");
        Matcher matcher = pattern.matcher(email);
        isValidEmail = matcher.matches();

        return isValidEmail;
    }

    private boolean isValidPhone(String phone) throws SQLException {
        boolean isValidPhone = false;
        // write code here
        Pattern pattern = Pattern.compile("\\+\\d+\\s+\\d+");
        Matcher matcher = pattern.matcher(phone);
        isValidPhone = matcher.matches();

        return isValidPhone;
    }
    
    boolean isValidData() throws SQLException {
        clearErrorLabels();
        boolean result = true; // assume all details are true, set to false if otherwise 
        // you need to check format deails and make sure they are consistent with DB 
       

        if (Fname_Field.getText().trim().isEmpty() || (Fname_Field.getText().trim().length() > 15)) {
            if (Fname_Field.getText().trim().isEmpty()) {
                Fname_Error.setText("Invalid. Cannot be empty.");
            } else if ((Fname_Field.getText().trim().length() > 15)) {
                Fname_Error.setText("Invalid. Must be < 15 chars.");
            }
            Fname_Error.setVisible(true);
            result = false;
        }

        if (Lname_Field.getText().trim().isEmpty() || (Lname_Field.getText().trim().length() > 15)) {
            if (Lname_Field.getText().trim().isEmpty()) {
                Lname_Error.setText("Invalid. Cannot be empty.");
            } else if ((Lname_Field.getText().trim().length() > 15)) {
                Lname_Error.setText("Invalid. Must be < 15 chars.");
            }
            Lname_Error.setVisible(true);
            result = false;
        }

        if (Address_Field.getText().trim().isEmpty() || (Address_Field.getText().trim().length() > 100)) {
            if (Address_Field.getText().trim().isEmpty()) {
                Address_Error.setText("Invalid. Cannot be empty.");
            } else if (Address_Field.getText().trim().length() > 100) {
                Address_Error.setText("Invalid. Must be < 100 chars.");
            }
            Address_Error.setVisible(true);
            result = false;
        }
        
        if (Email_Field.getText().trim().isEmpty() || !isValidEmail(Email_Field.getText())) {
            if (Email_Field.getText().trim().isEmpty()) {
                Email_Error.setText("Invalid. Cannot be empty.");
            } else if (!isValidEmail(Email_Field.getText())){
                Email_Error.setText("Email format must be like example@example.com");
            }
            Email_Error.setVisible(true);
            result = false;
        }
                
        if (PhoneNo_Field.getText().trim().isEmpty() || !isValidPhone(PhoneNo_Field.getText()) ) {
            if (PhoneNo_Field.getText().trim().isEmpty()) {
                PhoneNo_Error.setText("Invalid. Cannot be empty.");     
            } else if (!isValidPhone(PhoneNo_Field.getText())){
                PhoneNo_Error.setText("phone number must be like +xxx xxxxxxx");
            }
            PhoneNo_Error.setVisible(true);
            result = false;
        }

        return result;
    }
       
