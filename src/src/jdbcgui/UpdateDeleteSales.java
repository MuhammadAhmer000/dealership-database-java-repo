    private void populateFields() {
        try {
            SaleID_Field.setText(rs.getString("saleid"));
            Warranty_Field.setText(rs.getString("warrantyyears"));
            SDate_Field.setText(rs.getString("sdate"));
            cmbEmpID.setSelectedItem(rs.getString("empid"));
            cmbVIN.setSelectedItem(rs.getString("vin"));
            
            EnableDisableButtons();
        } catch (SQLException ex) {
            Logger.getLogger(UpdateDeleteSales.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void MoveNext() {
        try {
            // TODO add your handling code here:
            if (!rs.isLast()) {
                rs.next();
                populateFields();
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateDeleteSales.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
