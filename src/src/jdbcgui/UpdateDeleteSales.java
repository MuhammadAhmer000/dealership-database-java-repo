    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:\
        int confirmUpdate = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this sale?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
           if (confirmUpdate == JOptionPane.YES_OPTION)
           {
                       try {
            // make the result set scrolable forward/backward updatable
            String prepSQL = "DELETE proj_sales WHERE saleid = " + SaleID_Field.getText().trim();
            int result = dbCon.executePrepared(prepSQL);
            if (result > 0) {
                javax.swing.JLabel label = new javax.swing.JLabel("Sale No " + SaleID_Field.getText().trim() + " deleted successfully.");
                label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                JOptionPane.showMessageDialog(null, label, "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                getNewData();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error deleting Sale. Deletion is restricted");
            System.out.println(e);
        }
           }

    }//GEN-LAST:event_btnDeleteActionPerformed

