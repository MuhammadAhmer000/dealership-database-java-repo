    private void getNewData() {

        try {
            String str;
            // populate deptno field
            rs = dbCon.executeStatement("SELECT SALEid, VIN, SDATE, WARRANTYYEARS, EMPID FROM proj_SALES ORDER BY SALEID ASC "); // TODO: CONFIRM w/ DR. WISSAM

            rsVIN = dbCon.executeStatement("SELECT vin FROM PROJ_vehicles ORDER BY vin ASC "); // TODO: CONFIRM w/ DR. WISSAM
//            rsdeptno = statement.executeQuery("SELECT deptno, dname FROM dept ORDER BY deptno ASC ");
            cmbVIN.removeAllItems();
            while (rsVIN.next()) {
                cmbVIN.addItem(rsVIN.getString("vin"));
            }
            
            rsEmp = dbCon.executeStatement("SELECT EMPID FROM PROJ_EMPLOYEES ORDER BY EMPID ASC "); // TODO: CONFIRM w/ DR. WISSAM
            cmbEmpID.removeAllItems();
            while (rsEmp.next()) {
                cmbEmpID.addItem(rsEmp.getString("empid"));
            }

            // populate rest of fields
            rs.beforeFirst();
            rs.first();
            populateFields();
        } catch (SQLException e) {
            javax.swing.JLabel label = new javax.swing.JLabel("SQL Error - Display selected sale.");
            System.out.println(e);
            label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
            JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

