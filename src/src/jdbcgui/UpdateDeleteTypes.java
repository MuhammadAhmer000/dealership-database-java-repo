    public boolean isInteger(String s) {
        try {
            Integer.parseInt(s);
            return true;
        } catch (NumberFormatException ex) {
            return false;
        }
    }

    public boolean isDouble(String s) {
        try {
            Double.parseDouble(s);
            return true;
        } catch (NumberFormatException ex) {
            return false;
        }
    }

    boolean isValidData() {
        clearErrorLabels();
        boolean result = true;
        if (TypeID_Field.getText().trim().isEmpty() || !isInteger(TypeID_Field.getText().trim())) {
            if (TypeID_Field.getText().trim().isEmpty()) {
                TypeID_Error.setText("Invalid. Cannot be empty.");
            } else if (!isInteger(TypeID_Field.getText().trim())) {
                TypeID_Error.setText("Invalid. Must be integer.");
            }
            TypeID_Error.setVisible(true);
            result = false;
        }
        if (Type_Field.getText().trim().isEmpty() || (Type_Field.getText().trim().length() > 10)) {
            if (Type_Field.getText().trim().isEmpty()) {
                Type_Error.setText("Invalid. Cannot be empty.");
            } else if ((Type_Field.getText().trim().length() > 10)) {
                Type_Error.setText("Invalid. Must be < 10 chars.");
            }

            Type_Error.setVisible(true);
            result = false;
        }

 
        return result;
    }

