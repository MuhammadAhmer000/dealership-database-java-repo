    private void populateFields() {
        try {
            TypeID_Field.setText(rs.getString("typeid"));
            Type_Field.setText(rs.getString("type"));

            
            EnableDisableButtons();
        } catch (SQLException ex) {
            Logger.getLogger(UpdateDeleteTypes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void MoveNext() {
        try {
            // TODO add your handling code here:
            if (!rs.isLast()) {
                rs.next();
                populateFields();
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateDeleteTypes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
