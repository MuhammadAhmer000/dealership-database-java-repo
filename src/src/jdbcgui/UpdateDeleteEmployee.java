    boolean isValidData() {
        clearErrorLabels();
        boolean result = true;
        if (EmpID_Field.getText().trim().isEmpty() || !isInteger(EmpID_Field.getText().trim())) {
            if (EmpID_Field.getText().trim().isEmpty()) {
                Empno_Error.setText("Invalid. Cannot be empty.");
            } else if (!isInteger(EmpID_Field.getText().trim())) {
                Empno_Error.setText("Invalid. Must be integer.");
            }
            Empno_Error.setVisible(true);
            result = false;
        }
        if (Fname_Field.getText().trim().isEmpty() || (Fname_Field.getText().trim().length() > 10)) {
            if (Fname_Field.getText().trim().isEmpty()) {
                Fname_Error.setText("Invalid. Cannot be empty.");
            } else if ((Fname_Field.getText().trim().length() > 10)) {
                Fname_Error.setText("Invalid. Must be < 10 chars.");
            }

            Fname_Error.setVisible(true);
            result = false;
        }

        if (Lname_Field.getText().trim().isEmpty() || (Lname_Field.getText().trim().length() > 10)) {
            if (Fname_Field.getText().trim().isEmpty()) {
                Lname_Error.setText("Invalid. Cannot be empty.");
            } else if ((Fname_Field.getText().trim().length() > 10)) {
                Lname_Error.setText("Invalid. Must be < 10 chars.");
            }

            Lname_Error.setVisible(true);
            result = false;
        }

        if (Address_Field.getText().trim().isEmpty() || (Address_Field.getText().trim().length() > 9)) {
            if (Address_Field.getText().trim().isEmpty()) {
                Address_Error.setText("Invalid. Cannot be empty.");
            } else if (Address_Field.getText().trim().length() > 9) {
                Address_Error.setText("Invalid. Must be < 9 chars.");
            }
            Address_Error.setVisible(true);
            result = false;
        }

        if (BDate_Field.getText().trim().isEmpty() || !isValidDateFormat(BDate_Field.getText())) {
            if (BDate_Field.getText().trim().isEmpty()) {
                Bdate_Error.setText("Invalid. Cannot be empty.");
            } else if (!isValidDateFormat(BDate_Field.getText())) {
                Bdate_Error.setText("Invalid. Must be number.");
            }
            result = false;
        }

        if (Salary_Field.getText().trim().isEmpty() || !(isInteger(Salary_Field.getText().trim()) || isDouble(Salary_Field.getText().trim()))) {
            if (Salary_Field.getText().trim().isEmpty()) {
                Salary_Error.setText("Invalid. Cannot be empty.");
            } else if (!(isInteger(Salary_Field.getText().trim()) || isDouble(Salary_Field.getText().trim()))) {
                Salary_Error.setText("Invalid. Must be number.");
            }

            Salary_Error.setVisible(true);
            result = false;
        }

       
        return result;
    }

