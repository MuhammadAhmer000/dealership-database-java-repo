    private void getNewData() {

        try {
            String str;
            // populate deptno field
            rs = dbCon.executeStatement("SELECT empid, fname, lname, address, sex, bdate, salary, dno FROM proj_employees ORDER BY empid ASC "); // TODO: CONFIRM w/ DR. WISSAM

            rsdeptno = dbCon.executeStatement("SELECT deptno, dname FROM dept ORDER BY deptno ASC "); // TODO: CONFIRM w/ DR. WISSAM
//            rsdeptno = statement.executeQuery("SELECT deptno, dname FROM dept ORDER BY deptno ASC ");
            cmbDno.removeAllItems();
            while (rsdeptno.next()) {
                cmbDno.addItem(rsdeptno.getString("deptno"));
            }
            
            rsSex = dbCon.executeStatement("SELECT unique sex from proj_employees order by sex asc"); // TODO: CONFIRM w/ DR. WISSAM
            cmbSex.removeAllItems();
            while (rsSex.next()) {
                cmbSex.addItem(rsSex.getString("sex"));
            }

            // populate rest of fields
            rs.beforeFirst();
            rs.first();
            populateFields();
        } catch (SQLException e) {
            javax.swing.JLabel label = new javax.swing.JLabel("SQL Error - Display selected empid.");
            System.out.println(e);
            label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
            JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

