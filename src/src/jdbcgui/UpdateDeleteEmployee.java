    private void populateFields() {
        try {
            EmpID_Field.setText(rs.getString("empid"));
            Fname_Field.setText(rs.getString("fname"));
            Lname_Field.setText(rs.getString("lname"));
            
            BDate_Field.setText(rs.getString("bdate"));
            Salary_Field.setText(rs.getString("salary"));
            Address_Field.setText(rs.getString("address"));
            cmbSex.setSelectedItem(rs.getString("sex"));
            cmbDno.setSelectedItem(rs.getString("dno"));
            
            EnableDisableButtons();
        } catch (SQLException ex) {
            Logger.getLogger(UpdateDeleteEmployee.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void MoveNext() {
        try {
            // TODO add your handling code here:
            if (!rs.isLast()) {
                rs.next();
                populateFields();
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateDeleteEmployee.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
