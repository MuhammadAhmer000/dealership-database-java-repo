    private void getNewData() {

        try {
            String str;
            // populate deptno field
            rs = dbCon.executeStatement("SELECT dnumber, dname, mgrid, mgrstartdate FROM proj_departments ORDER BY dnumber ASC "); // TODO: CONFIRM w/ DR. WISSAM

            rsMgr = dbCon.executeStatement("SELECT empid FROM proj_employees ORDER BY empid ASC "); // TODO: CONFIRM w/ DR. WISSAM
//            rsdeptno = statement.executeQuery("SELECT deptno, dname FROM dept ORDER BY deptno ASC ");
            cmbMgr.removeAllItems();
            while (rsMgr.next()) {
                cmbMgr.addItem(rsMgr.getString("empid"));
            }
            

            // populate rest of fields
            rs.beforeFirst();
            rs.first();
            populateFields();
        } catch (SQLException e) {
            javax.swing.JLabel label = new javax.swing.JLabel("SQL Error - Display selected deptid.");
            System.out.println(e);
            label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
            JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

