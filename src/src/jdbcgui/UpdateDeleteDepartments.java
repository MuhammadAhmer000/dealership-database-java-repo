    boolean isValidData() {
        clearErrorLabels();
        boolean result = true;
        if (DeptID_Field.getText().trim().isEmpty() || !isInteger(DeptID_Field.getText().trim())) {
            if (DeptID_Field.getText().trim().isEmpty()) {
                Deptno_Error.setText("Invalid. Cannot be empty.");
            } else if (!isInteger(DeptID_Field.getText().trim())) {
                Deptno_Error.setText("Invalid. Must be integer.");
            }
            Deptno_Error.setVisible(true);
            result = false;
        }
        if (Dname_Field.getText().trim().isEmpty() || (Dname_Field.getText().trim().length() > 15)) {
            if (Dname_Field.getText().trim().isEmpty()) {
                Dname_Error.setText("Invalid. Cannot be empty.");
            } else if ((Dname_Field.getText().trim().length() > 15)) {
                Dname_Error.setText("Invalid. Must be < 15 chars.");
            }

            Dname_Error.setVisible(true);
            result = false;
        }

        if (MgrDate_Field.getText().trim().isEmpty() || !isValidDateFormat(MgrDate_Field.getText())) {
            if (MgrDate_Field.getText().trim().isEmpty()) {
                MgrDate_Error.setText("Invalid. Cannot be empty.");
          } else if (!isValidDateFormat(MgrDate_Field.getText())){
                MgrDate_Error.setText("Invalid. Date must be DD-MMM-YYYY.");
                MgrDate_Error.setVisible(true);
            }

            MgrDate_Error.setVisible(true);
            result = false;
        }

     
 
        return result;
    }

