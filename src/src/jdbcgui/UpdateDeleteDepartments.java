    private void populateFields() {
        try {
            DeptID_Field.setText(rs.getString("dnumber"));
            Dname_Field.setText(rs.getString("dname"));
            MgrDate_Field.setText(rs.getString("mgrstartdate"));            
            cmbMgr.setSelectedItem(rs.getString("mgrid"));
            
            EnableDisableButtons();
        } catch (SQLException ex) {
            Logger.getLogger(UpdateDeleteDepartments.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void MoveNext() {
        try {
            // TODO add your handling code here:
            if (!rs.isLast()) {
                rs.next();
                populateFields();
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateDeleteDepartments.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
